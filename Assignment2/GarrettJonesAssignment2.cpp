#include <iostream>
using namespace std;

int main()
{
    cout << "    * " << endl;
    cout << "   *** " << endl;
    cout << "  ***** " << endl;
    cout << " ******* " << endl;
    cout << endl;
    
    //The escape directive \n can also be used
    
    cout << "    * \n";
    cout << "   *** \n";
    cout << "  ***** \n";
    cout << " ******* \n";
    cout << endl;
    return 0;
}